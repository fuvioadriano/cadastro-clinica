    private void preencheComboPaciente() {
        DefaultComboBoxModel cboPacientes = (DefaultComboBoxModel) cboPaciente.getModel();
        cboPacientes.removeAllElements();

        try {
            pacientes = adao.getListaPaciente();

            for (Paciente p : pacientes) {
                cboPacientes.addElement(p.getNome());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "combo" + ex.getMessage() + ex.getCause());
        }
    }

