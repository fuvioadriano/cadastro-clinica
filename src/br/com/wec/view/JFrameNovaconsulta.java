    private void jButtonSalvarAgendamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarAgendamentoActionPerformed
        // TODO add your handling code here:

        java.util.Date dt = jclData.getDate();

        Agenda a = new Agenda();

        a.setData(new java.sql.Date(dt.getTime()));

        a.setDthora(new java.sql.Date(new Date().getTime()));

        /*
         *********************************
         **Pegar string da data atual*****
         *********************************
         */
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");

        Date today = jclData.getDate();

        String reportDate = df.format(today);

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        String dateInString = reportDate + " " + cboHora.getSelectedItem().toString();

        Date date = new Date();

        try {
            date = formatter.parse(dateInString);

        } catch (ParseException ex) {
            Logger.getLogger(JFrameNovaconsulta.class.getName()).log(Level.SEVERE, null, ex);
        }

        /**
         * *****************************
         */
        a.setHi(new java.sql.Date(date.getTime()));

        /**
         * ****************
         * Duracao ****************
         */
        String h = "";
        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(date);

        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        gc.add(Calendar.MINUTE, Integer.parseInt(cboDuracao.getSelectedItem().toString()));
        h = sdf.format(gc.getTime());

        dateInString = reportDate + " " + h;

        try {
            date = formatter.parse(dateInString);

        } catch (ParseException ex) {
            Logger.getLogger(JFrameNovaconsulta.class.getName()).log(Level.SEVERE, null, ex);
        }
        /*
         ************************
         */

        a.setHf(new java.sql.Date(date.getTime()));

        a.setMedico(getIdMedico(cboMedico.getSelectedItem().toString()));
        a.setPaciente(getIdPaciente(cboPaciente.getSelectedItem().toString()));
        
        a.setStatus(1);

        try {
            adao.inserir(a);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        preencheGrid();
    }//GEN-LAST:event_jButtonSalvarAgendamentoActionPerformed

    private int getIdPaciente(String nome) {
        int id = 0;

        for (Paciente p : pacientes) {
            if (nome == p.getNome()) {
                id = p.getId();
            }
        }
        return id;
    }

    private int getIdMedico(String nome) {
        int id = 0;

        for (Medico m : medicos) {

            if (nome == m.getNome()) {
                id = m.getId();
            }

        }
        return id;
    }


