    private void preencheHora() {
        // TODO add your handling code here:
        cboHora.removeAllItems();

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        String dateInString = "24/11/2014 " + HI;

        Date date = new Date();
        try {
            date = formatter.parse(dateInString);

        } catch (ParseException ex) {
            Logger.getLogger(JFrameNovaconsulta.class.getName()).log(Level.SEVERE, null, ex);
        }

        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(date);

        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        //System.out.println(sdf.format(gc.getTime()));

        //gc.add(Calendar.MINUTE, 30);
        //System.out.println(sdf.format(gc.getTime()));
        String h = "";
        int duracao = 30;

        while (!h.equals(HF)) {

            if (!h.equals("")) {

                gc.add(Calendar.MINUTE, DUR);

                duracao = duracao + 30;
            }

            h = sdf.format(gc.getTime());

            cboHora.addItem(h);
        }
    }

