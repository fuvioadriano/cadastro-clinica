    private Paciente getForm() throws ParseException {
        Paciente p = new Paciente();
        p.setBairro(txtBairro.getText());
        p.setCidade(txtCidade.getText());
        p.setCpf(txtCPF.getText());

        java.util.Date dt = jdtDtNasc.getDate();

        p.setDtnasc(new java.sql.Date(dt.getTime()));
        
        
        p.setEmail(txtEmail.getText());
        p.setEndereco(txtEndereco.getText());
        p.setEstado(cboEstado.getSelectedItem().toString());
        p.setNome(txtNome.getText());
        p.setTelefone(txtTelefone.getText());
        return p;
    }

    private void setForm(Paciente p) {
        txtBairro.setText(p.getBairro());
        txtCidade.setText(p.getCidade());
        txtCPF.setText(p.getCpf());
        jdtDtNasc.setDate(p.getDtnasc());
        txtEmail.setText(p.getEmail());
        txtEndereco.setText(p.getEndereco());
        cboEstado.setSelectedItem(p.getEstado());
        txtNome.setText(p.getNome());
        txtTelefone.setText(p.getTelefone());
    }


    private void jButtonSalvarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarPacienteActionPerformed
        // TODO add your handling code here:
        Paciente p;
        String msg = "";
        try {
            p = this.getForm();
            if ("".equals(action)) {
                pdao.inserir(p);
                msg = "Paciente inserido com sucesso!";
            } else {
                p.setId(id);
                pdao.atualizar(p);
                msg = "Paciente atualizado com sucesso!";
            }
            
            jp.getLista();
            
            //setDefaultCloseOperation(this.EXIT_ON_CLOSE);
            
           
            
            
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
        
        
        // setDefaultCloseOperation(JFrameNovoPaciente.DISPOSE_ON_CLOSE);
        dispose();

    }//GEN-LAST:event_jButtonSalvarPacienteActionPerformed

