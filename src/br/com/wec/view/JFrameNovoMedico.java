    private void getListaEspecialidade() {
        List<Especialidade> lista;
        try {
            lista = mdao.getListaEspecialidades();
            for (int i = 0; i < lista.size(); i++) {
                cboEspecialidade.addItem(lista.get(i).getDescricao());
               
            }
        } catch (Exception ex) {
            Logger.getLogger(JFrameNovoMedico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setForm(Medico m) throws ParseException {
        txtBairro.setText(m.getBairro());
        txtCidade.setText(m.getCidade());
        txtCpf.setText(m.getCpf());
        txtCrm.setText(m.getCrm());
        jdtDtContrato.setDate(m.getDtcontrato());
        jdtNasc.setDate(m.getDtnasc());
        txtEmail.setText(m.getEmail());
        txtEndereco.setText(m.getEndereco());
        try {
            cboEspecialidade.setSelectedItem(mdao.getListaEspecialidades(m.getEspecialidade()).getDescricao());
        } catch (Exception ex) {
            Logger.getLogger(JFrameNovoMedico.class.getName()).log(Level.SEVERE, null, ex);
        }

        cboEstado.setSelectedItem(m.getEstado());

        txtNome.setText(m.getNome());
        cboSexo.setSelectedItem(m.getSexo());
        txtTelefone.setText(m.getTelefone());
    }

    private Medico getForm() {
        Medico m = new Medico();

        m.setBairro(txtBairro.getText());
        m.setCidade(txtCidade.getText());
        m.setCpf(txtCpf.getText());
        m.setCrm(txtCrm.getText());

        java.util.Date dt = jdtDtContrato.getDate();
        m.setDtcontrato(new java.sql.Date(dt.getTime()));

        dt = jdtNasc.getDate();
        m.setDtnasc(new java.sql.Date(dt.getTime()));
        m.setEmail(txtEmail.getText());
        m.setEndereco(txtEndereco.getText());

        try {
            m.setEspecialidade(mdao.getListaEspecialidades(cboEspecialidade.getSelectedItem().toString()).getId());
        } catch (Exception ex) {
            Logger.getLogger(JFrameNovoMedico.class.getName()).log(Level.SEVERE, null, ex);
        }

        m.setEstado(cboEstado.getSelectedItem().toString());
        m.setNome(txtNome.getText());
        m.setSexo(cboSexo.getSelectedItem().toString());
        m.setTelefone(txtTelefone.getText());

        return m;
    }

