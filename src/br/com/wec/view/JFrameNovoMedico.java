    private void jButtonSalvarMedicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarMedicoActionPerformed
        // TODO add your handling code here:

        Medico m;
        String msg = "";
        try {
            m = this.getForm();
            if ("".equals(action)) {
                mdao.inserir(m);
                msg = "Medico inserido com sucesso!";
            } else {
                m.setId(id);
                mdao.atualizar(m);
                msg = "Medico atualizado com sucesso!";
            }

            jm.getLista();

            //setDefaultCloseOperation(this.EXIT_ON_CLOSE);
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }

        // setDefaultCloseOperation(JFrameNovoPaciente.DISPOSE_ON_CLOSE);
        dispose();
    }//GEN-LAST:event_jButtonSalvarMedicoActionPerformed

